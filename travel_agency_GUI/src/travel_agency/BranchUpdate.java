    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       conn=DBconnection.ConnectDB();
        
         String brstreet = jTextField1.getText();
         
         String bn = jTextField2.getText();
         int brnumber = Integer.parseInt(bn);
         
         String city = jTextField3.getText();
         
         String myQuery = "INSERT INTO branch VALUES (NULL, ?, ?, ?) ";
         
     
          try{
                 pst=conn.prepareStatement(myQuery);
                 pst.setString(1,brstreet);
                 pst.setInt(2,brnumber);
                 pst.setString(3,city);
                 pst.executeUpdate();
                 JOptionPane.showMessageDialog(null, "The insert was successfull");
                 jTextField1.setText("");
                 jTextField2.setText("");
                 jTextField3.setText("");
               
          }
    
             catch (SQLException ex) {
          JOptionPane.showMessageDialog(null, ex);
          }
          
    
    }//GEN-LAST:event_jButton1ActionPerformed

