    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
         conn=DBconnection.ConnectDB();
         
         String myQuery= "SELECT * FROM branch WHERE br_code = ? ";
         String myQuery1= "DELETE FROM branch WHERE br_code = ? ";
          
         String brid = jTextField4.getText();
         int bid = Integer.parseInt(brid);
         
         try{
             pst=conn.prepareStatement(myQuery);
                pst.setInt(1,bid);
                rs=pst.executeQuery();
                
                if(rs.next()){
             
                pst=conn.prepareStatement(myQuery1);
                pst.setInt(1,bid);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "The removal of the branch was successfull");
                jTextField4.setText("");
                }
                else {
                JOptionPane.showMessageDialog(null, "There is no Branch with this code");
                }
         }
         
         catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
         } 
         
    }//GEN-LAST:event_jButton5ActionPerformed

